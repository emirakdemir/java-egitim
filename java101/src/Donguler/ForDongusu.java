package Donguler;

public class ForDongusu {
    public static void main(String[] args) {
        for (int i =3; i <= 10; i +=2){
            System.out.println(i);
        }
        System.out.println("-----");
        int k = 3;
        while (k <= 10){
            System.out.println(k);
            k +=2 ;
        }
    }
}
